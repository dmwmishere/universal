DROP TABLE MEMES IF EXISTS;


CREATE TABLE MEMES(ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL PRIMARY KEY,NAME VARCHAR(64) NOT NULL,TYPE VARCHAR(32),YEAR INTEGER,STATUS VARCHAR(16),ORIGIN VARCHAR(64),BADGE VARCHAR(32),INSERTED TIMESTAMP DEFAULT CURRENT_TIMESTAMP); 

DROP TABLE OPERATIONS IF EXISTS;

CREATE TABLE OPERATIONS(UUID VARCHAR(36) NOT NULL,OPERATIONID INTEGER NOT NULL,STARTTIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,STOPTIME TIMESTAMP,DATA VARCHAR(64),STATE INTEGER);
